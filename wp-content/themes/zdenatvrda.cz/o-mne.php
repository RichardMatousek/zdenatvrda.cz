<h3>Více o mně</h3>
<p>S kulturistikou jsem začala ve 30 letech a dosáhla těchto úspěchů:</p>
<ul>
  <li>Dvojnásobná mistryně světa (1995, 1996).</li>
  <li>Mistryně Evropy (1995).</li>
  <li>6. násobná mistryně České republiky.</li>
  <li>Absolutní vítězka GP Oděsa 1998.</li>
  <li>Účastnice nejvyšší profesionální soutěže žen, Olympia 1997 v New Yorku a 1998 v Praze.</li>
</ul>
<p>Po šestileté přestávce jsem se vrátila k závodní kariéře, kterou jsem rozšířila o novou soutěžní disciplínu - &bdquo;bodyfitness&ldquo;</p>
<p>Na Grand Prix Nutrend - 4. 6. 2005 jsem skončila druhá v kategorii bodyfitness a nominovala se na Mistroství Evropy masters v Polsku, kde jsem získala stříbrnou medaili.
<p>V roce 2005 jsem se zúčastnila Mistrovství světa masters v kategorii bodyfitness v Budapešti, kde jsem skončila na 4. místě a v roce 2006 se zúčastnila Mistroství světa na Sicílii v téže kategorii, kde jsem jsem skončila opět na 4. místě. Na Mistrovství Evropy žen 2007 v kategorii bodyfitness v Čačaku jsem z 25 závodnic skončila na 9. místě a na Mistroství světa v Santa Suzaně v záží 2007 jsem skončila z 25 závodnic do 35 let na 11. místě.
<ul>
   <li>2006 - Mistroství světa bodyfitness a fitness na Sicílii – 4. místo </li>
   <li>2008 – Mistrovství světa bodyfitness a fitness v Santa Suzaně – 4. místo</li>
   <li>2010 – Mistrovství světa juniorů a masters v Mexiku v kateg. nad 163cm  - 8. místo </li>
   <li>2012 – Mistrovství světa juniorů a masters v Budapešti  - 5. místo</li>
   <li>2014 – Mistrovství světa juniorů a masters v Mexiku, kategorie bodyfitness - 8. místo</li>
</ul>
<p><span class="zvyraznene">Pravidelně každým rokem pořádám v Brně mezinárodní soutěž Grand Prix Fitness.</span> Spolu s PhDr. Karlou Tománkovou, CSc. jsem autorkou metodického materiálu DVD - „Kondiční posilování v tělesné výchově na základních a středních školách“.</p>


